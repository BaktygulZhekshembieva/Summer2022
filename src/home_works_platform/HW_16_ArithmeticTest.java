package home_works_platform;

public class HW_16_ArithmeticTest {
    public static void main(String[] args) {

        int n1 = 98;
        int n2 = 5;

        int sum = n1 + n2;
        int dif = n1 - n2;
        int prod = n1 * n2;
        int div = n1 / n2;
        int rem = n1 % n2;

        System.out.println("Sum: " + sum + "\nDifference: " + dif + "\nProduct: " + prod + "\nDivision: " + div + "\nRemainder: " + rem);
    }
}
